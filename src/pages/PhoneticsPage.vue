        <div class="text-cont">
            <p class="descr-4">The following pinyin consonants are very similar (if not identical) to their English
                equivalents and
                should not
                give you much trouble: b, p, m, f, d, t, n, l.

                The two notable exceptions to this rule are b and d. You may find it difficult at first to
                distinguish
                between
                Mandarin's b and p sounds as well as its d and t sounds. This is because in English "b" and "d"
                sounds
                are
                voiced,meaning that the vocal chords are used when producing the sound. This creates a somewhat more
                forceful
                consonant sound. In Mandarin Chinese, the only difference between the b sound and the p sound is
                that
                the p
                sound is aspirated, meaning that a puff of air comes from the mouth when pronouncing the p sound.
                (In
                English,
                the "p" sound is also aspirated.) This makes the difference between Mandarin b and p seem subtler,
                since
                aspiration and non-aspiration is the only difference, as opposed to aspiration and voicing, as in
                English.

                Just like b and p, the only difference between Mandarin's d and t sounds is that the d sound is not
                aspirated
                while the t sound is. Neither consonant sound is voiced in Mandarin, while the "d" sound is voiced
                in
                English.

                While difficult to distinguish at first, this subtle difference will become more and more apparent
                with
                prolonged exposure.</p>
